function galeriaCiclob(){
        
        var cuadro=document.getElementById('galeriaCiclob');
        
        if(cuadro.style.display == 'none'){
        
        setTimeout(function(){ cuadro.style.display = "block";}, 10);
              
        setTimeout(function(){ cuadro.style.opacity = "1";}, 550);
    }
    
    else{
        
        var cuadro=document.getElementById('galeriaCiclob');
        
        setTimeout(function(){ cuadro.style.opacity = "0";}, 10);
              
        setTimeout(function(){ cuadro.style.display = "none";}, 550);
        
    }
    
            }
            
